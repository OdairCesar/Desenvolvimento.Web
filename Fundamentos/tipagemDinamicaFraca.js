let qualquer = 'Legal' //Tipo string
console.log(qualquer)
console.log(typeof qualquer)

qualquer = 3.1516 //Tipo number
console.log(qualquer)
console.log(typeof qualquer)

qualquer = 57 // No JS não a diferença entre inteiro e decimal igual outras linguagens
console.log(qualquer)
console.log(typeof qualquer)

qualquer = true // Tipo booleano
console.log(qualquer)
console.log(typeof qualquer)

/* O tipo da variavel pode ser mudada sem problemas. Diferente de outras linguagens */
var variavel = 1
console.log(variavel)
var variavel = "ola mundo"
console.log(variavel)